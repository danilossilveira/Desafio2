package Desafio2;

public class GrausCelsius {
    public static void main(String[] args) {
        int temp = 8;
        double tfahrenhei = (temp * 1.8) + 32;
        System.out.println(temp + " graus celsius é equivalente a "+tfahrenhei +" graus Fahrenheit");
    }
}


/*Dica: A fórmula para converter temperaturas de graus
Celsius para Fahrenheit é: (temperatura * 1.8) + 32.
 */